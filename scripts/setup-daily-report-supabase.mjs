import { spawnSync } from 'node:child_process';
import { randomBytes } from 'node:crypto';
import { readFileSync, writeFileSync, existsSync, unlinkSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPORT_TO = 'hasimozturk@gmail.com';

function loadEnvFile() {
  const envPath = resolve(__dirname, '..', '.env');
  if (!existsSync(envPath)) return;
  let c = readFileSync(envPath, 'utf8');
  if (c.charCodeAt(0) === 0xfeff) c = c.slice(1);
  for (const line of c.split(/\r?\n/)) {
    const m = line.match(/^\s*([A-Za-z0-9_]+)\s*=\s*(.*?)\s*$/);
    if (!m) continue;
    if (process.env[m[1]] === undefined) {
      process.env[m[1]] = m[2].replace(/^["']|["']$/g, '').trim();
    }
  }
}

function loadProjectRef() {
  const p = resolve(__dirname, '..', 'supabase', '.temp', 'project-ref');
  if (existsSync(p)) return readFileSync(p, 'utf8').trim();
  const env = process.env.SUPABASE_PROJECT_REF?.trim();
  if (env) return env;
  throw new Error('project-ref yok: önce `npx supabase link`');
}

function run(cmd, args) {
  const r = spawnSync(cmd, args, { stdio: 'inherit', shell: process.platform === 'win32' });
  if (r.status !== 0) process.exit(r.status ?? 1);
}

function main() {
  loadEnvFile();
  const args = process.argv.slice(2).filter((a) => !a.startsWith('--'));
  const resendOnly = process.argv.includes('--resend-only');
  const resendKey = (args[0] || process.env.RESEND_API_KEY || '').trim();
  if (!resendKey || !resendKey.startsWith('re_')) {
    console.error(`
Resend API anahtarı gerekli.

Kolay yol (terminale yapıştırmayın):
  1) Proje kökündeki .env dosyasını açın (Cursor sol panel)
  2) En alta ekleyin:  RESEND_API_KEY=re_xxxxxxxx
  3) Kaydedin, terminalde yalnızca:  npm run setup:daily-report

Alternatif: Supabase Dashboard → Project Settings → Edge Functions → Secrets
  → RESEND_API_KEY = re_...  (sonra --resend-only ile deploy gerekmez)

Resend: https://resend.com → ${REPORT_TO} → API Keys → Create
`);
    process.exit(1);
  }

  const projectRef = loadProjectRef();
  const cronSecret = randomBytes(24).toString('hex');
  const projectUrl = `https://${projectRef}.supabase.co`;

  console.log('Proje:', projectRef);
  console.log('Alıcı:', REPORT_TO);

  if (resendOnly) {
    run('npx', [
      'supabase',
      'secrets',
      'set',
      '--project-ref',
      projectRef,
      `RESEND_API_KEY=${resendKey}`,
    ]);
    console.log('\nRESEND_API_KEY Supabase Edge\'e yazıldı. Cron zaten kuruluysa yarın 20:00\'de mail gelir.');
    console.log('Hemen test: Dashboard → Edge Functions → daily-admin-report → Invoke (x-daily-report-cron header gerekir)');
    return;
  }

  run('npx', ['supabase', 'functions', 'deploy', 'daily-admin-report', '--project-ref', projectRef]);

  run('npx', [
    'supabase',
    'secrets',
    'set',
    '--project-ref',
    projectRef,
    `RESEND_API_KEY=${resendKey}`,
    `DAILY_REPORT_CRON_SECRET=${cronSecret}`,
  ]);

  const vaultSql = `-- generated by setup-daily-report-supabase.mjs
select cron.unschedule('daily_admin_report_2000_tr')
where exists (select 1 from cron.job where jobname = 'daily_admin_report_2000_tr');

select vault.create_secret('${projectUrl}', 'daily_report_project_url');
select vault.create_secret('${cronSecret}', 'daily_report_cron_secret');

select cron.schedule(
  'daily_admin_report_2000_tr',
  '0 17 * * *',
  $$
  select net.http_post(
    url := (select decrypted_secret from vault.decrypted_secrets where name = 'daily_report_project_url')
           || '/functions/v1/daily-admin-report',
    headers := jsonb_build_object(
      'Content-Type', 'application/json',
      'x-daily-report-cron', (select decrypted_secret from vault.decrypted_secrets where name = 'daily_report_cron_secret')
    ),
    body := '{}'::jsonb
  ) as request_id;
  $$
);
`;

  const tmpVault = resolve(__dirname, '..', 'database', 'setup', '_daily_report_vault_generated.sql');
  writeFileSync(tmpVault, vaultSql, 'utf8');
  try {
    run('npx', ['supabase', 'db', 'query', '--linked', '-f', tmpVault]);
  } finally {
    try {
      unlinkSync(tmpVault);
    } catch {
      /* ignore */
    }
  }

  console.log('\n--- Test (hemen mail) ---');
  console.log(
    `curl -s -X POST "${projectUrl}/functions/v1/daily-admin-report" -H "x-daily-report-cron: ${cronSecret}" -H "Content-Type: application/json" -d "{}"`,
  );
  console.log('\nKurulum tamam. Cron: 17:00 UTC = 20:00 TSİ.');
}

main();
